import { signInWithPopup } from "firebase/auth";
import { auth, provider } from "../../firebase";
import { useNavigate } from "react-router-dom";
import { useState } from "react";

export default function Auth() {
  const navigate = useNavigate();
  const [loading, setLoading] = useState(false);

  const googleLogin = async () => {
    try {
      setLoading(true);
      const result = await signInWithPopup(auth, provider);
      const user = result.user;

      // ‚úÖ Save user locally
      localStorage.setItem("user", JSON.stringify({
        name: user.displayName,
        email: user.email,
        photo: user.photoURL
      }));

      navigate("/register"); // ‚úÖ Redirect after login

    } catch (err) {
      alert("Google Login Failed");
    } finally {
      setLoading(false);
    }
  };

  return (
    <div className="min-h-screen bg-gradient-to-br from-slate-950 via-slate-900 to-slate-950 flex flex-col items-center justify-center p-6 relative overflow-hidden">
      {/* Animated Background Elements */}
      <div className="absolute inset-0 overflow-hidden">
        <div className="absolute -top-40 -left-40 w-80 h-80 bg-blue-500/10 rounded-full blur-3xl animate-pulse"></div>
        <div className="absolute top-1/2 -right-20 w-96 h-96 bg-emerald-500/10 rounded-full blur-3xl animate-pulse" style={{ animationDelay: '1s' }}></div>
        <div className="absolute bottom-20 left-1/4 w-64 h-64 bg-purple-500/10 rounded-full blur-3xl animate-pulse" style={{ animationDelay: '2s' }}></div>
      </div>

      {/* Logo/Header */}
      <div className="relative z-10 text-center mb-8">
        <div className="inline-flex p-4 bg-gradient-to-r from-sky-500/10 to-emerald-500/10 rounded-3xl mb-6">
          <span className="text-4xl">üîê</span>
        </div>
        <h1 className="text-4xl md:text-5xl font-bold bg-gradient-to-r from-sky-400 to-emerald-400 bg-clip-text text-transparent mb-3">
          Welcome to Numerano
        </h1>
        <p className="text-slate-400 text-lg">The Official Mathematics Club of DSCE</p>
      </div>

      {/* Login Card */}
      <div className="relative z-10 w-full max-w-md">
        <div className="bg-gradient-to-br from-slate-900/80 to-slate-950/80 backdrop-blur-sm rounded-2xl shadow-2xl p-8 border border-slate-800/50">
          {/* Card Header */}
          <div className="text-center mb-8">
            <h2 className="text-2xl font-bold text-white mb-2">Sign In to Continue</h2>
            <p className="text-slate-400">Access your team dashboard and registration</p>
          </div>

          {/* Google Login Button */}
          <button
            onClick={googleLogin}
            disabled={loading}
            className="w-full bg-gradient-to-r from-slate-800/50 to-slate-900/50 hover:from-slate-700/50 hover:to-slate-800/50 border border-slate-700/50 hover:border-slate-600/50 p-4 rounded-xl text-white font-medium transition-all duration-300 flex items-center justify-center gap-4 group disabled:opacity-50 disabled:cursor-not-allowed"
          >
            {loading ? (
              <>
                <div className="animate-spin rounded-full h-5 w-5 border-t-2 border-b-2 border-white"></div>
                <span>Signing in...</span>
              </>
            ) : (
              <>
                <div className="p-2 bg-white/10 rounded-lg group-hover:bg-white/20 transition-colors">
                  <svg className="w-6 h-6" viewBox="0 0 24 24">
                    <path fill="#4285F4" d="M22.56 12.25c0-.78-.07-1.53-.2-2.25H12v4.26h5.92c-.26 1.37-1.04 2.53-2.21 3.31v2.77h3.57c2.08-1.92 3.28-4.74 3.28-8.09z"/>
                    <path fill="#34A853" d="M12 23c2.97 0 5.46-.98 7.28-2.66l-3.57-2.77c-.98.66-2.23 1.06-3.71 1.06-2.86 0-5.29-1.93-6.16-4.53H2.18v2.84C3.99 20.53 7.7 23 12 23z"/>
                    <path fill="#FBBC05" d="M5.84 14.09c-.22-.66-.35-1.36-.35-2.09s.13-1.43.35-2.09V7.07H2.18C1.43 8.55 1 10.22 1 12s.43 3.45 1.18 4.93l2.85-2.22.81-.62z"/>
                    <path fill="#EA4335" d="M12 5.38c1.62 0 3.06.56 4.21 1.64l3.15-3.15C17.45 2.09 14.97 1 12 1 7.7 1 3.99 3.47 2.18 7.07l3.66 2.84c.87-2.6 3.3-4.53 6.16-4.53z"/>
                  </svg>
                </div>
                <span className="text-lg">Continue with Google</span>
              </>
            )}
          </button>
        </div>

        {/* Footer Links */}
        <div className="mt-8 text-center">
          <p className="text-slate-500 text-sm">
            Need help?{' '}
            <a href="mailto:support@numerano.com" className="text-sky-400 hover:text-sky-300 transition-colors">
              Contact Support
            </a>
          </p>
          <p className="text-slate-600 text-xs mt-2">
            By continuing, you agree to our{' '}
            <a href="#" className="text-slate-500 hover:text-slate-400 transition-colors">Terms</a>
            {' '}and{' '}
            <a href="#" className="text-slate-500 hover:text-slate-400 transition-colors">Privacy Policy</a>
          </p>
        </div>
      </div>

      {/* Decorative Elements */}
      <div className="absolute bottom-10 left-10 w-16 h-16 bg-gradient-to-r from-sky-500/5 to-transparent rounded-full blur-xl"></div>
      <div className="absolute top-10 right-10 w-20 h-20 bg-gradient-to-r from-emerald-500/5 to-transparent rounded-full blur-xl"></div>
    </div>
  );
}
